package javaprogram;

public class Decrement {
	public static void main(String[] args) {
		
	
	 int l= 6;
	int m= 9;
	int n= 13;
	int k= ++l - m-- + l++ + n++ + ++m;
    
	System.out.println(k--);
	System.out.println(l);	
	System.out.println(m);
	System.out.println(n);
	System.out.println(k);



	

	
	}
}
